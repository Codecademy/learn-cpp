// Function to invoke endgame
void endgame(std::string answer, std::string codeword)
{
    if (answer == codeword)
  {
    std::cout << "\nHooray! You saved the person and earned a medal of honor!\n";
  }
  else 
  {
    std::cout<< "\nOh no! The UFO just flew away with another person!\n";
  }
  
  std::cout << "\nCodeword was: " << codeword << "\n";

}

// Function to display the correct and incorrect guesses. Also displays current progress with uncovering the codeword.
void display_status(std::vector<char> incorrect, std::string answer)
{
  std::cout << "\nIncorrect Guesses: ";
  
  for (int i=0; i< incorrect.size(); i++)
  {
    std::cout << incorrect[i] << " ";
  }

  std::cout << "\nCodeword: ";

  for (int j=0; j< answer.size(); j++)
  {
    std::cout << answer[j] << " ";
  } 
}


